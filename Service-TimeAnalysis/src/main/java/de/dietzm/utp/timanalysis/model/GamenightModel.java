package de.dietzm.utp.timanalysis.model;

public class GamenightModel {

	private int segmentid;
	
	private double speed;
	
	private double speed_ref;
	
	private double speed_difference;

	public int getSegmentid() {
		return segmentid;
	}

	public void setSegmentid(int segmentid) {
		this.segmentid = segmentid;
	}

	public double getSpeed() {
		return speed;
	}

	public void setSpeed(double speed) {
		this.speed = speed;
	}

	public double getSpeed_ref() {
		return speed_ref;
	}

	public void setSpeed_ref(double speed_ref) {
		this.speed_ref = speed_ref;
	}

	public double getSpeed_difference() {
		return speed_difference;
	}

	public void setSpeed_difference(double speed_difference) {
		this.speed_difference = speed_difference;
	}
	
	
}
